save_lmm_diagnostics <- function(model, outcome_label) {

  # Extracts fitted values ​​and residuals from the model.
  d <- data.frame(
    fitted = fitted(model),
    resid = resid(model)
  )

  # --- Plot A: Residuals vs Fitted (detects patterns and heteroscedasticity) ---
  p1 <- ggplot2::ggplot(d, ggplot2::aes(x = fitted, y = resid)) +
    ggplot2::geom_point() +
    ggplot2::geom_hline(yintercept = 0) +
    ggplot2::labs(
      title = paste0("Residuals vs Fitted (", outcome_label, ")"),
      x = "Fitted values",
      y = "Residuals"
    )

  ggplot2::ggsave(
    filename = paste0("outputs/figures/diagnostics/Diag_ResidualsFitted_", outcome_label, ".png"),
    plot = p1,
    width = 7, height = 5, dpi = 300
  )

  # --- Plot B: Normal Q-Q plot (assesses approximate normality of residues)) ---
  qq <- stats::qqnorm(d$resid, plot.it = FALSE)
  qq_df <- data.frame(theoretical = qq$x, sample = qq$y)

  p2 <- ggplot2::ggplot(qq_df, ggplot2::aes(x = theoretical, y = sample)) +
    ggplot2::geom_point() +
    ggplot2::geom_abline() +
    ggplot2::labs(
      title = paste0("Normal Q-Q Plot (", outcome_label, ")"),
      x = "Theoretical quantiles",
      y = "Sample quantiles"
    )

  ggplot2::ggsave(
    filename = paste0("outputs/figures/diagnostics/Diag_QQ_", outcome_label, ".png"),
    plot = p2,
    width = 7, height = 5, dpi = 300
  )
}

