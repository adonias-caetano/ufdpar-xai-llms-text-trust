fit_lmm <- function(data, y) {
  fml <- stats::as.formula(paste0(y, " ~ type_of_explanation + (1 | id)"))
  lme4::lmer(fml, data = data, REML = FALSE)
}

models <- setNames(lapply(outcomes, \(y) fit_lmm(df, y)), outcomes)

extract_contrast <- function(model, outcome_name) {
  emm <- emmeans::emmeans(model, ~ type_of_explanation)
  ctr <- emmeans::contrast(emm, method = "revpairwise")  # CoT - Non-CoT
  as.data.frame(summary(ctr, infer = TRUE)) %>%
    dplyr::mutate(Outcome = stringr::str_to_title(outcome_name)) %>%
    dplyr::rename(
      Contrast = contrast,
      Estimate = estimate,
      SE = SE,
      DF = df,
      CI_Lower = lower.CL,
      CI_Upper = upper.CL,
      T_value = t.ratio,
      P_value = p.value
    ) %>%
    dplyr::select(Outcome, Contrast, Estimate, SE, DF, CI_Lower, CI_Upper, T_value, P_value)
}

lmm_tbl <- dplyr::bind_rows(lapply(names(models), \(nm) extract_contrast(models[[nm]], nm))) %>%
  dplyr::mutate(
    OutcomeRole = dplyr::if_else(Outcome == "Trust", "Primary", "Secondary/Exploratory")
  )

secondary <- lmm_tbl %>%
  dplyr::filter(Outcome != "Trust") %>%
  dplyr::mutate(P_Holm = stats::p.adjust(P_value, method = "holm"))

