# Sensitivity - Paired Wilcoxon signed-rank tests
wide <- df %>%
  dplyr::select(id, type_of_explanation, all_of(outcomes)) %>%
  tidyr::pivot_wider(
    names_from = type_of_explanation,
    values_from = all_of(outcomes),
    names_sep = "_"
  )
wilcox_paired <- function(wide_data, y) {
  x <- wide_data[[paste0(y, "_CoT")]]
  z <- wide_data[[paste0(y, "_Non-CoT")]]

  ok <- stats::complete.cases(x, z)
  x <- x[ok]; z <- z[ok]

  if (length(x) < 2) {
    return(tibble::tibble(Outcome = stringr::str_to_title(y), N_Pairs = length(x), P_value = NA_real_))
  }

  wt <- suppressWarnings(stats::wilcox.test(x, z, paired = TRUE, exact = FALSE))
  tibble::tibble(Outcome = stringr::str_to_title(y), N_Pairs = length(x), P_value = wt$p.value)
}

wilcox_tbl <- dplyr::bind_rows(lapply(outcomes, \(y) wilcox_paired(wide, y))) %>%
  dplyr::mutate(
    OutcomeRole = dplyr::if_else(Outcome == "Trust", "Primary", "Secondary/Exploratory")
  )

wilcox_secondary <- wilcox_tbl %>%
  dplyr::filter(Outcome != "Trust") %>%
